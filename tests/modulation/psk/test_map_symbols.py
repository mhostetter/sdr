def test_bpsk_bin():
    phi = np.rad2deg(2.081994752721715)
    psk = sdr.PSK(2, phase_offset=phi, symbol_labels="bin")
    s = np.array([0, 1, 0, 1, 1, 0])
    x = psk.map_symbols(s)
    x_truth = np.array(
        [
            -0.489222815711927 + 0.872158836787711j,
            0.489222815711927 - 0.872158836787711j,
            -0.489222815711927 + 0.872158836787711j,
            0.489222815711927 - 0.872158836787711j,
            0.489222815711927 - 0.872158836787711j,
            -0.489222815711927 + 0.872158836787711j,
        ]
    )
    np.testing.assert_array_almost_equal(x, x_truth)


